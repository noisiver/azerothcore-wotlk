DROP TABLE IF EXISTS `progression_world`.`disables`;
CREATE TABLE `progression_world`.`disables` (
    `sourceType` INT(10) UNSIGNED NOT NULL,
    `entry` INT(10) UNSIGNED NOT NULL,
    `flags` TINYINT(3) UNSIGNED NOT NULL DEFAULT '0',
    `params_0` VARCHAR(255) NOT NULL DEFAULT '' COLLATE 'utf8mb4_unicode_ci',
    `params_1` VARCHAR(255) NOT NULL DEFAULT '' COLLATE 'utf8mb4_unicode_ci',
    `comment` VARCHAR(255) NOT NULL DEFAULT '' COLLATE 'utf8mb4_unicode_ci',
    `MinPatch` INT UNSIGNED NOT NULL DEFAULT '0',
    `MaxPatch` INT UNSIGNED NOT NULL DEFAULT '21',
    PRIMARY KEY (`sourceType`, `entry`, `MinPatch`, `MaxPatch`) USING BTREE
)
COLLATE='utf8mb4_unicode_ci'
ENGINE=InnoDB;

INSERT INTO `progression_world`.`disables`
SELECT `sourceType`, `entry`, `flags`, `params_0`, `params_1`, `comment`, 20 AS `MinPatch`, 21 AS `MaxPatch` FROM `base_world`.`disables` WHERE `sourceType`=1 AND `entry` IN (
    550, -- Battle of Hillsbrad
    6145, -- The Crimson Courier
    6521, -- An Unholy Alliance
    6522 -- An Unholy Alliance
)
UNION SELECT `sourceType`, `entry`, `flags`, `params_0`, `params_1`, `comment`, 17 AS `MinPatch`, 21 AS `MaxPatch` FROM `base_world`.`disables` WHERE `sourceType`=1 AND `entry` IN (
    3631, -- Summon Felsteed
    4487, -- Summon Felsteed
    4488, -- Summon Felsteed
    4489, -- Summon Felsteed
    4490 -- Summon Felsteed
);

